create table brands
(
    id                    bigint generated by default as identity not null primary key,
    customer_phone_number varchar(50) unique                      not null,
    name                  varchar(255)                            not null,
    social_media_link VARCHAR(255),
    brand_values_character VARCHAR(255),
    target_audience VARCHAR(255),
    contact_person_name VARCHAR(255),
    founder_interests VARCHAR(255),
    category VARCHAR(255),
    subscriber_count INT,
    geo VARCHAR(255),
    count_ball INT,
    count_like BIGINT,
    username_id VARCHAR(255),
    tariff_id BIGINT,
    foreign key (tariff_id) references tariffs (id),
    foreign key (customer_phone_number) references customers (phone_number)
);
